//Pergunte ao usuário qual é o dia da semana. Se a resposta for "Sábado" ou "Domingo", mostre "Bom fim de semana!". Caso contrário, mostre "Boa semana!".

diaDaSemana = prompt('Qual é o dia da semana?'); // Solicita ao usuário que insira o dia da semana
if (diaDaSemana == 'Sábado') { // Se o dia for "Sábado"
    alert('Bom fim de semana!');
} else if (diaDaSemana == 'Domingo') { // Se o dia for "Domingo"
    alert('Bom fim de semana!'); 
} else {
    alert('Boa semana!');
}